def get_calories_declension(n):
    n = abs(n) % 100  # Берем последние две цифры
    n1 = n % 10       # Берем последнюю цифру

    if 10 < n < 20:  # Для чисел от 10 до 19 всегда "калорий"
        return "калорий"
    elif n1 == 1:     # Если последняя цифра 1 (кроме 11), "калория"
        return "калория"
    elif 2 <= n1 <= 4:  # Если последняя цифра 2, 3, 4 (кроме 12, 13, 14), "калории"
        return "калории"
    else:              # В остальных случаях "калорий"
        return "калорий"
